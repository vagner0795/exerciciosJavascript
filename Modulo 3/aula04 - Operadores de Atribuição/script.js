//! Operadores de Atribuição

/*  

 = -> Igualdade
 += -> Adição
 -= -> Subtração
 /= -> Divisão
 *= -> Multiplicação
 %= -> Resto

 */  

 let result1 = 20

 result1 += 70 //? result1 = result1 + 70

 console.log(result1)

 let result2 = 20

 result2 -= 70 //? result2 = result2 - 70

 console.log(result2)

 let result3 = 100

 result3 /= 70 //? result3 = result3 / 70

 console.log(result3)

 let result4 = 500

 result4 *= 70 //? result4 = result4 * 70

 console.log(result4)

 let result5 = 2000

 result5 %= 70 //? result5 = result5 % 70

 console.log(result5)




