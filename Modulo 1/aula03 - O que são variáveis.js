// O que são variáveis?
// São formas de armazenar informações que podem ser strings, numbers, booleans



// Var armazena constantes e variaveis (DESCONTINUADO )
// Let - armazena variaveis - valor pode ser declarado ou alterado
// Const - armazena constantes - valor é fixo

// o mais comum é usar const, mas podemos usar let em casos de valores que serão alterados

// Nomes de variáveis - Não pode começar com numeros, não pode ter espaços
// normalmente começa com letra minúscula

let numeroDeAlunosSalaSeis = 35
let frutas = "abacaxi, banana, melão"
let verdade = true

const person = "João"
const age = 22
const sex = "Male"

// executando valores das variáveis

console.log(numeroDeAlunosSalaSeis)
console.log(frutas)
console.log(verdade)
console.log(person)
console.log(age)
console.log(sex)
